L_HEARTBEAT_ASK_OPTIN() {
  cat <<'EOF'
Утренний дайджест — короткое ежедневное сообщение: что сделано, что ждёт
вас. Да/нет сейчас, позже можно передумать. Одна честная строка о том, как
это работает: он только ЧИТАЕТ ваше рабочее пространство, чтобы составить
сводку — никогда не совершает действий сам и ничего никуда не отправляет,
кроме этого дайджеста вам. Рекомендация: да.
EOF
  printf 'Включить утренний дайджест? Да/нет (y/n), по умолчанию y: '
}
L_HEARTBEAT_ON() { printf '  утренний дайджест — установлен и ВКЛЮЧЁН\n'; }
L_HEARTBEAT_OFF() { printf '  утренний дайджест — установлен, но ВЫКЛЮЧЕН. Включить в любой момент:\n      bash install.sh --enable-heartbeat\n'; }
L_HEARTBEAT_NO_LAUNCHD() {
  printf 'ЗАМЕТКА: на этом компьютере нет "launchctl" (обычно вне macOS), поэтому\n'
  printf 'утренний дайджест нельзя запланировать автоматически. Больше ничего не\n'
  printf 'сломалось — запускайте вручную в любой день: bash %s/.qroky/heartbeat.sh\n' "$1"
