L_UPDATE_AVAILABLE() {
  local from="$1" to="$2" changelog="$3"
  cat <<EOF
Доступна новая версия правил, которым следует ваш ассистент: $from -> $to
Что улучшится у вас:
$changelog
Чтобы принять сейчас:   bash install.sh --apply-update
Чтобы узнать подробнее: bash install.sh --show-update-details
Чтобы решить позже:     ничего не делайте — увидите это сообщение снова в следующий раз
EOF
}
L_UPDATE_NONE() { printf '  обновлений нет — у вас последняя версия\n'; }
L_UPDATE_CONFLICT() {
  printf '\nВ вашей копии есть локальные изменения, которые затронет обновление.\n'
  printf 'Ничего не перезаписано. Вот точно, что отличается:\n'
}
L_UPDATE_ASK_CONFIRM() { printf 'Применить это обновление сейчас? Введите "да" для подтверждения, что угодно ещё — отмена: '; }
L_UPDATE_APPLIED() { printf '  обновление применено: %s -> %s. Запись сохранена сюда: %s\n' "$1" "$2" "$3"; }
L_UPDATE_CANCELLED() { printf '  обновление отменено — ничего не изменилось\n'; }
