L_TELEGRAM_WALKTHROUGH() {
  cat <<'EOF'
  Давайте создадим вашего собственного бота в Telegram — это займёт около 2 минут:
    1. Откройте приложение Telegram.
    2. В строке поиска введите: BotFather (у него синяя галочка).
    3. Откройте чат с BotFather и отправьте: /newbot
    4. Он попросит имя — введите любое, какое нравится (например, "My Qroky").
    5. Он попросит username — должен заканчиваться на "bot" (например, "my_qroky_bot").
    6. BotFather ответит сообщением с длинным кодом — это ТОКЕН вашего бота.
       Выглядит так: 123456789:ABCdefGhIJKlmNoPQRsTUVwxyZ
    7. Скопируйте весь этот код.
EOF
}
L_TELEGRAM_ASK_TOKEN() { printf 'Вставьте токен сюда (или напишите "skip", чтобы пропустить): '; }
L_TELEGRAM_VALIDATING() { printf '  проверяю токен через Telegram...\n'; }
L_TELEGRAM_TOKEN_OK() { printf '  токен работает — подключено к боту: %s\n' "$1"; }
L_TELEGRAM_TOKEN_BAD() {
  printf '  этот токен не сработал. Частые причины:\n'
  printf '    - вместе с ним скопировался пробел или перенос строки — скопируйте ещё раз, внимательно\n'
  printf '    - токен уже был сброшен — откройте BotFather, отправьте /token, используйте новый\n'
  printf '  Попробуйте снова, или напишите "skip", чтобы пропустить этот шаг.\n'
}
L_TELEGRAM_STORED() { printf '  токен сохранён только на этом компьютере (никуда больше не отправляется): %s\n' "$1"; }

